import Foundation
import UIKit


class ColorsUtil {
    
    static func getLightBlue() -> UIColor{
        return UIColor(red: 115/255, green: 149/255, blue: 174/255, alpha: 1)
    }
    
    static func getBlue() -> UIColor{
        return UIColor(red: 85/255, green: 122/255, blue: 149/255, alpha: 1)
    }
    
    static func getBrown() -> UIColor{
        return UIColor(red: 93/255, green: 92/255, blue: 97/255, alpha: 1)
    }
    
    static func getEarthy() -> UIColor{
        return UIColor(red: 177/255, green: 162/255, blue: 150/255, alpha: 1)
    }
    
}
